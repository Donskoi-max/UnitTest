#include "Number.h"

double Number::Add(double number1, double number2) {
	double result = number1 + number2;
	return result;
}
double Number::Minus(double numbebr1, double numbebr2) {
	double result = numbebr1 - numbebr2;
	return result;
}
double Number::Multiply(double number1, double number2) {
	double result = number1 * number2;
	return result;
}
double Number::Divide(double numbebr1, double numbebr2) {
	double result = numbebr1 / numbebr2;
	return result;
}
